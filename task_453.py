# 453. Написати програму, яка приймає на вхід послідовність з цілих чисел, 
# після чого друкує значення всіх її елементів, піднесені до квадрату.
'''
Вхідні дані:
1 2 3 4 5 6 7 8 9

Вихідні дані:
1 4 9 16 25 36 49 64 81
'''
lst = []
lst = [int(item) for item in input("Enter the list members: ").split()]
for item in lst:
    print(item**2, end=" ")
print()