#Напишіть програму, яка отримує значення радіуса кола (дробове число) від
#користувача та обчислює площу круга і довжину колу. Виведення результату
#відбувається з трьома символами після десяткової крапки. Значення числа Пі
#округлити до сотих.
radius=float(input('Enter a radius: '))
s=3.14*radius*radius
l=2*3.14*radius
print(f'{s:.3f}')
