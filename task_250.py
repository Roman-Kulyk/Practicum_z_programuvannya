# 250. Визначте суму усіх елементів послідовності, яка завершується числом 0. Вводиться послідовність 
# цілих чисел, що закінчується числом 0 (саме число 0 в послідовність не входить, а використовується як 
# ознака її закінчення).

'''
Вхідні дані:
2
5
3
0

Вихідні дані:
10
'''

num_list = []
while True:
    n = int(input("Enter a number: "))
    if n != 0:
        num_list.append(n)
    elif n == 0:
        break
print(sum(num_list))