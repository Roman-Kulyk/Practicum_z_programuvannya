# 256.Напишіть програму, яка виводить усі трицифрові числа, сума цифр яких дорівнює деякому значенню n, 
# яке вводить користувач.
'''
Вхідні дані:

4

Вихідні дані:

112
121
130
202
211
220
301
310
400
'''
n = int(input('Enter a number: '))
num_list = []
while sum(num_list) == n and len(num_list) == 3:
    num_list.append()
    print(num_list)