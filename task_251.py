# 251. Визначте середнє значення всіх елементів послідовності, яка завершується числом 0. Вводиться 
# послідовність цілих чисел, що закінчується числом 0 (саме число 0 в послідовність не входить, а 
# використовується як ознака її закінчення).

'''
Вхідні дані:
3
4
5
0

Вихідні дані:
4.0
'''

num_list = []
while True:
    n = int(input("Enter a number: "))
    if n != 0:
        num_list.append(n)
    elif n == 0:
        break
print(sum(num_list)/len(num_list))