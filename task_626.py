# 626. Напишіть функцію для створення гістограми (наприклад, у вигляді *) 
# із заданого списку цілих чисел як у вихідних даних. 
# Формат введення списку чисел як у вхідних даних.
'''
Вхідні дані:

2,7,1,4,2,3,9,3

Вихідні дані:
**
*******
*
****
**
***
*********
***
'''

def min_max(*nums):
    for i in nums:
        print(i * '*')

min_max(2,7,1,4,2,3,9,3)