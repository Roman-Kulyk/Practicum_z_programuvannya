# 356. При заданому користувачем значенні цілого числа n ≥ 2 обчислити 
# суму 1 x 2 + 2 x 3 + ... + (n - 1) x n. Відповідь виведіть у вигляді 
# обчисленого виразу і його значення в точності, як показано у вихідних даних.
'''
Вхідні дані:
2
4

Вихідні дані:
1*2=2
1*2+2*3+3*4=20
'''
n = int(input("Enter a number: "))
sum = 0
for i in range(1,n):
    sum += i * (i + 1)
print('{} = {}'.format("*".join([str(i) for i in range(1,n)]),sum))