#160. Дано натуральне число n (1 ≤ n ≤ 9999), що визначає вартість товару в копійках. 
#Подайте вартість у гривнях і копійках. Якщо у результаті виходить 0 копійок, 
#то замість цього значення необхідно записати порожній рядок.

num = int(input("Enter a number: "))
coins = num % 100
hrn = num // 100

if coins == 0:
    print(str(hrn)," ")
else:
    print(str(hrn) +" "+ str(coins))