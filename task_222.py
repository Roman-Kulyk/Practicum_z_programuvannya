#222. Напишіть програму, яка друкує цілі числа від 1 до n (1 < n ≤ 1000) з такою умово:
# для чисел кратних 3 виводить *3* замість числа, для чисел кратних 5 друкує *5*, а для чисел, 
# які кратні 3 і 5 одночасно, повідомлення буде *35*.


n = int(input("Enter a number: "))
for i in range(1,n + 1):
    if i % 3 == 0 and i % 5 == 0:
        print("*35*")
    elif i % 5 == 0:
        print("*5*")
    elif i % 3 == 0 :
        print("*3*")
    else:
        print(i)