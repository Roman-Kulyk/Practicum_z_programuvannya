#224. Напишіть програму для знаходження тих чисел із діапазону 1 ≤ n ≤ 1000 
# (число n вводить користувач), які кратні 7 і 5, і виведення таких чисел в один рядок.


n = int(input("Enter a number: "))
for i in range(1, n + 1):
    if i % 5 == 0 and i % 7 == 0:
        print(i, end = ' ')
    else:
        continue
print()