# 249.Програма отримує на вхід послідовність цілих невід’ємних чисел, кожне число вводиться в окремому 
# рядку. Послідовність завершується числом 0, при зчитуванні якого програма повинна закінчити свою роботу і 
# вивести кількість членів послідовності (не рахуючи завершального числа 0).
'''
Вхідні дані:

8
4
6
0

Вихідні дані:

3

'''
num_list = []
while True:
    n = int(input("Enter a number: "))
    if n != 0:
        num_list.append(n)
    elif n == 0:
        break
print(len(num_list))
