# 290. Дано натуральне число n. Визначити номер деякої заданої цифри d в ньому, 
# відраховуючи від кінця числа. Якщо такої цифри немає, відповіддю має бути число 0;
# якщо таких цифр в числі кілька, повинен бути визначений номер позиції найправішої 
# з них.
'''
Вхідні дані:

1233572
3

Вихідні дані:

4

'''

n = input("Enter a number: ")
d = input("Enter a number: ")

n_list = list(n)
if d not in n_list:
    print(0)
else: 
    print(len(n_list) - n_list.index(str(d)))