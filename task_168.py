#168. Дано трицифрове ціле число. Визначити суму першої і останньої цифр числа і порівняти її із значенням 
#другої цифри числа. Відповідно вивести повідомлення: >, < і =.


num = int(input("Enter a number: "))
f = num // 100
s = num % 100 // 10
t = num % 100 % 10

if f + t > s:
    print(">")
elif f + t == s:
    print("=")
else:
    print("<")