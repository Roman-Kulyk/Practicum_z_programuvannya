# 443. Циклічно посуньте елементи списку вправо (0-вий елемент переходить на місце 
# 1-го, 1-ий на місце 2-го, ..., останній елемент переходить на місце 0-го). Вводиться 
# список чисел. Всі числа списку знаходяться на одному рядку.
'''
Вхідні дані:
2 0 2 5
6
1 2 3

Вихідні дані:
5 2 0 2
6
3 1 2
'''
lst = []
lst = [int(item) for item in input('Enter the list members: ').split()]
last = lst.pop()
lst.insert(0,last)
for item in lst:
    print(item, end = ' ')
print()