# 230. Напишіть програму, в якій користувач вводить цілі числа. Якщо буде введено ціле число n, 
# програма має завершити своє виконання повідомленням Done. Спочатку користувач вводить число n, 
# а потім решту чисел.


n = int(input("Enter a number: "))
while True:
    n_1 = int(input("Enter a number: "))
    if n_1 == n:
        print("Done")
        break
    else:
        continue
