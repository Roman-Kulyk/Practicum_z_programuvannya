#77.Виконайте перетворення значення температури у градусах Цельсія (С) для
#інших температурних шкал: Фаренгейта (F) і Кельвіна (K).Програма повинна
#відображати еквівалентну температуру у градусах Фаренгейта (F = 32 + 9/5 * C)
#і у градусах Кельвіна (K = C + 273,15).

C = int(input('Enter a value in C: '))
F = 32 + 9/5*C
K = C + 273.15
print(f'{C} Celsius = {F} Fahrenheit = {K} Kelvin'.format(C,F,K))
