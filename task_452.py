# 452. Напишіть програму для обчислення добутку цілих чисел (без використання циклу 
# for), які вводяться через пропуск користувачем в одному рядку.
'''
Вхідні дані:
2 5 3

Вихідні дані:
30
'''
num = input("Enter the list members: ").split()
product = 1
for n in num:
    product *= int(n)
print(product)