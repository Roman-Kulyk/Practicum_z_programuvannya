# 270. Дано натуральне число n. Визначити, чи є воно автоморфним числом. Примітка. 
# Автоморфне число - число, квадрат якого рівний останнім розрядами квадрата цього числа: 
# 5 - 25, 6 - 36, 25 - 625.
'''
Вхідні дані:

9376
26

Вихідні дані:

True
False

'''
n = int(input("Enter a number: "))
n_a = int(input("Enter a number: "))
if n % 10 == n_a % 10:
    print(True)
else:
    print(False)

    
