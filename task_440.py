# 440. Знайти відстань між двома точками із заданими координатами (x1, y1, x2, y2) 
# в 2-вимірному просторі. Значення координат є цілими числами. Результат округлити до 
# двох знаків після десяткової крапки.
'''
Вхідні дані:

2 3 -7 4

Вихідні дані:
9.06
'''
lst = []
lst = [int(item) for item in input("Enter the list members: ").split()]

d = ((lst[2] - lst[0] ) ** 2 + (lst[3] - lst[1] ) ** 2) ** 0.5
print(f'{d:.2f}')