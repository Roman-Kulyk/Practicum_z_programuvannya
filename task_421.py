# 421. Вводиться список цілих чисел в одному рядку через пропуск. Надрукуйте 
# кількість елементів, які є за значенням більші за обох їх сусідів. Перший і останній 
# елементи списку не слід розглядати, оскільки вони не мають двох сусідів.
'''
Вхідні дані:
1 2 1
1 3 2 4 2
1 2 3

Вихідні дані:
1
2
0
'''
lst = list(map(int,input("Enter the list members: ").split()))

count = 0
for i in range(1,len(lst)-1):
    if lst[i] > lst[i -1] and lst[1] > lst[i + 1]:
        count +=1
print(count)