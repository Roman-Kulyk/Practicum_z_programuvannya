# 289. Скласти програму для графічного зображення подільності чисел від 1 до n 
# (значення n вводиться з клавіатури). У кожному рядку треба надрукувати чергове 
# число і стільки символів +, скільки є дільників у цього числа.
'''
Вхідні дані:

5

Вихідні дані:

1+
2++
3++
4+++
5++
'''
n = int(input("Enter a number:"))
for i in range(0,n):
    i+=1
    print(i)