# 276. Є монотонна послідовність, в якій кожне натуральне число k зустрічається рівно k разів: 
# 1, 2, 2, 3, 3, 3, 4, 4, 4, 4, .... За введеним натуральним числом n виведіть перші n членів цієї 
# послідовності.
'''
Вхідні дані:

5

Вихідні дані:

1 2 2 3 3

'''
n = int(input("Enter a number: "))
num_list = []
count = 1
for i in range(n):
    num_list.append(count)
    if len(num_list)>= count:
        count += 1
    print(num_list[:n])