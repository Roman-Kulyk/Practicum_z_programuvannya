# 693.Напишіть рекурсивну функцію, яка обчислює суму цілих чисел a і b. З арифметичних операцій 
# використовується тільки додавання одиниці і віднімання одиниці.
"""
Вхідні дані:
2
9
2
-5
3
0

Вихідні дані:
11
-3
3
"""
def summ(a,b):
    if b == 0:
        return a
    elif b > 0:
        return summ(a + 1, b -1 )
    else:
        return summ(a - 1, b + 1)

result = summ(2, -5)
print(result)